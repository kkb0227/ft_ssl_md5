char	*ft_strrchr(const char *s, int c)
{
	char *str;

	str = (char *)s;
	while (*str != '\0')
		str++;
	while (*str != c)
	{
		if (str == (char *)s)
			return (0);
		str--;
	}
	return (str);
}
