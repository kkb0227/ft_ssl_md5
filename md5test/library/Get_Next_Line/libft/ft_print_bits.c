#include "libft.h"

void	ft_print_bits(unsigned char octet)
{
	int div;

	div = 128;
	while (div >= 1)
	{
		if ((octet / div) == 1)
		{
			write(1, "1", 1);
			octet -= div;
		}
		else
			write(1, "0", 1);
		div /= 2;
	}
}
